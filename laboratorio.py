import math
def suma(n1,n2):
    return n1+n2
def resta(n1,n2):
    return n1-n2
def multipliacion (n1,n2):
    return n1*n2
def division(n1,n2):
    return n1/n2
def exponenciacion(n1,n2):
    return n1**n2
def raiz(n):
    return math.sqrt(n)
def suma_5numeros(n1,n2,n3,n4,n5):
    return (n1+n2+n3+n4+n5)
def residuo (n1,n2):
    return (n1%n2)
def quetzal_a_dolar (n):
    return n/7.91
def dolar_a_quetzal (n):
    return n*7.91


